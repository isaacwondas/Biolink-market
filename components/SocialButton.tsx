export default function SocialButton({
  platform,
  handle,
  vendorId,
}: SocialButtonProps) {
  const handleSocialClick = () => {
    if (typeof window === "undefined") return;

    const width = window.innerWidth;
    const deviceType =
      width < 768 ? "mobile" : width < 1024 ? "tablet" : "desktop";

    // Build destination URL
    const cleanHandle = handle.replace(/^@/, "").trim();
    let destinationUrl = "";

    // If handle is already a full URL, use it directly
    if (cleanHandle.startsWith("http")) {
      destinationUrl = cleanHandle;
    } else {
      switch (platform) {
        case "Instagram":
          destinationUrl = `https://instagram.com/${cleanHandle}`;
          break;
        case "Facebook":
          destinationUrl = `https://facebook.com/${cleanHandle}`;
          break;
        case "TikTok":
          destinationUrl = `https://tiktok.com/@${cleanHandle}`;
          break;
        case "YouTube":
          destinationUrl = `https://youtube.com/@${cleanHandle}`;
          break;
        case "Twitter":
          destinationUrl = `https://x.com/${cleanHandle}`;
          break;
        case "LinkedIn":
          destinationUrl = `https://linkedin.com/in/${cleanHandle}`;
          break;
        case "Snapchat":
          destinationUrl = `https://snapchat.com/add/${cleanHandle}`;
          break;
        case "Telegram":
          destinationUrl = `https://t.me/${cleanHandle}`;
          break;
        case "Pinterest":
          destinationUrl = `https://pinterest.com/${cleanHandle}`;
          break;
        case "Threads":
          destinationUrl = `https://threads.net/@${cleanHandle}`;
          break;
        case "WhatsApp":
          destinationUrl = `https://wa.me/${cleanHandle}`;
          break;
        default:
          destinationUrl = handle;
          break;
      }
    }

    if (!destinationUrl) return;

    try {
      navigator.sendBeacon(
        "/api/log-click",
        JSON.stringify({
          vendor_id: vendorId,
          platform: platform.toLowerCase(),
          device_type: deviceType,
        }),
      );
    } catch (err) {
      console.error("Telemetry error:", err);
    }

    window.open(destinationUrl, "_blank", "noopener,noreferrer");
  };

  const style = BRAND_STYLES[platform] || DEFAULT_STYLE;

  return (
    <button
      onClick={handleSocialClick}
      title={`Follow on ${platform}`}
      className={`
        w-9 h-9 rounded-full flex items-center justify-center
        ${style.bg} ${style.hover} ${style.text}
        transition-all duration-150 active:scale-95 shadow-sm
      `}
    >
      <PlatformIcon platform={platform} />
    </button>
  );
}
